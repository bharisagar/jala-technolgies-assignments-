package strings;



public class trim {
	   public static void main(String args[])
	    {
	        
	      // trims the trailing and leading spaces 
	    String s = " sagar is good boy  "; 
	    System.out.println(s.trim());  
	      
	    // trims the leading spaces 
	    s = " Chetna loves reading books"; 
	    System.out.println(s.trim());  
	      
	       
	    }


}
